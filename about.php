                    <hr>
                    <h2 class="intro-text text-center">Caracteristicile socializării în adolescenţă</h2>
                    <hr>
                </div>

                <div class="col-md-6">
                    <img class="img-responsive img-border-left" src="img/dljvAd.jpg" alt="">
                </div>
                <div class="col-md-6">
                <p>Adolescenţa este deschisă de preadolescenţă sau pubertate (11-15 ani), urmată de adolescenţa propriu-zisă (15-18 ani). Debutul adolescenţei, cum este considerată perioada pubertară, este marcat de o scădere a stimei de sine. Este perioada vârstei critice, a crizei sau revoluţiei adolescenţilor, când moştenirea culturală este desacralizată, tânărul situându-se într-un vid cultural, datorită simţului său critic deosebit de ascuţit. Ieşirea din conformismul infantil face ca autoevaluarea să nu mai găsească suport, ceea ce a fost important în perioada anterioară nu mai este actual, iar noile opţiuni sunt încă în faza de tatonare. Se produce o deplasarea de interes dinspre mediul securizant al familiei spre cel al grupurilor de egali, ceea ce face să apară nelinişti, depresii, anxietate.</p>
                </div>
                <div class="col-md-12">
                <p>Problema dificilă în această perioadă o constituie concilierea impulsurilor sexuale deosebit de puternice cu modelele de comportament deja instalate în stadiile precedente şi cu sistemul de constrângeri şi presiuni culturale datorate distanţării în timp a maturizării biologice de cea socială. Există la adolescenţi o mare nevoie afectivă, dorinţă de a fi acceptaţi, iubiţi, preţuiţi, dar se opun tendinţei de a fi acaparaţi. Este vârsta la care tânărul se află în căutarea identităţii, manifestând tendinţe contradictorii, oscilând între egocentrism şi altruism, solitudine şi socialitate. El se caută fără a se regăsi. În fapt n-are cum să se regăsească fiindcă încă n-a fost. El este doar proiect, nu este, ci va fi. De aici instabilitatea afectivă, nonconformismul, indisciplina, agresivitatea, protestul, ostilitatea la intruziunea în spaţiul intimităţii confuze. Astfel de atitudini port fi uşor etichetate ca tendinţe spre devianţă. 

Criza adolescenţilor sau conflictul dintre generaţii este datorat şi situării în timpi psihologici diferiţi. Adultul este situat inconştient pe dimensiunea timpului prezent, compus din clipe egale, iar „adolescentul are viitorul în sânge” (Tofler).
Adolescenţa este marcată de tendinţe aparent contradictorii: nevoia de afiliere şi nevoia de intimitate, dar nu de singurătate – stare subiectivă generată de insatisfacţia relaţionării sociale. Cei apropiaţi, părinţii, dar şi profesorii, trebuie să respecte aceste nevoi. Uneori, actele deviante nu sunt decât manifestări ale creativităţii de protecţie, care permit tânărului o anumită identitate şi evitarea rigorilor convenţiei moralei adulte. Fuga, chiulul, vagabondajul, cerşetoria pot reprezenta forme de evaziune, aventuri eliberatoare sau, alteori, modalităţi de supravieţuire. Criteriul respectului necondiţionat al regulilor sau al strictei moralităţi este inaplicabil; până la un anumit punct, astfel de conduite pot fi expresia capriciilor vârstei.
La această vârstă, o importanţă deosebită în constituirea personalităţii o au identificările cu modelul, eroul, prietenul. De aici prieteniile durabile şi nevoia de a le îndruma cu tact. Educatorul, părinte sau profesor, trebuie să practice un stil mai puţin directiv, cu asocierea ideii de responsabilitate libertăţii şi încrederii reciproce. 
Stilul de educaţie autoritar şi represiv, care-şi propune învăţarea necondiţionată a consemnelor şi conformităţii, cu reprimarea oricăror opoziţii prin care tânărul îşi rezervă dreptul la autonomie morală, generează insecuritate afectivă, cu tendinţe spre evaziune, acte de violenţă şi agresivitate, conduite structurate defectuos, tendinţe conflictuale sau frustrante, comportamente marginale sau deviante, crize prelungite. Soluţionarea crizei adolescenţei conduce la maturizarea deplină, permiţând integrarea în colectivitatea adultă, în care familia de procreare, sistemul ocupaţional şi comunitatea locală prezintă importanţă deosebită. Integrarea în aceste noi colectivităţi impune interiorizarea de roluri inaccesibile până acum: rolul de soţ, soţie, rolul profesional, rolul de cetăţean, de enoriaş.

Socializarea nu se încheie aici, în adolescenţă, ci se continuă pe întreg parcursul vieţii, existând tranziţii de realizat, crize de depăşit.
</p>
