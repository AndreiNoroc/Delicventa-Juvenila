                    <h2 class="intro-text text-center">Variabile ale socializarii in familie si delicventa juvenila</h2>
                    <h4 class="intro-text text-center">Tipul familiei</h4>
                    <hr>
                </div>
                <div class="col-md-6">
                    <img class="img-responsive img-border-left" src="img/famDVJ.jpg" alt="">
                </div>
                <div class="col-md-6">

                    <p>O bună perioadă de timp, studiile privind eficienţa socializării, performanţa sau eşecul şcolar al copilului, comportamentul conformist sau delincvent, se structurau pe distincţia tranşantă dintre familia organizată şi cea dezorganizată. Dinamica familiei contemporane, marcată de diminuarea natalităţii, multiplicarea coabitărilor, creşterea numărului de copii născuţi în afara familiei, explozia ratei divorţialităţii (Stănciulescu, 1997, pp. 137 şi urm.), a repus în discuţie disjuncţia tradiţională, argumentând ideea că ceea ce este important pentru dezvoltarea copilului nu este atât forma sau tipul familiei („organizată” sau „dezorganizată”), cât funcţionalitatea acesteia, atmosfera sau climatul ei. Este şi motivul pentru care conceptul de familie „dezorganizată” tinde să fie înlocuit cu cel de familie „parentală” (sau monoparentală), pentru a sublinia existenţa relaţiei parentale independent de relaţia conjugală.</p>

                </div>
                <div class="col-md-12">
                    <p>Climatul familial este conceptul de maximă generalitate ce caracterizează atmosfera sau cadrul general de relaţionare interpersonală, putând fi surprins prin intermediul mai multor indicatori, cum sunt:</p>
                    <ul style="font-size:18px;">
                        <li> modul de raportare interpersonală a părinţilor (apropiere, dragoste, înţelegere şi sprijin reciproc, complementaritate de sarcini, comunicare, respect, acord în legătură cu problemele majore, sau dimpotrivă);</li>
                        <li> sistemul atitudinilor parentale în raport cu valorile preţuite;</li>
                        <li> dinamica şi modul de soluţionare a stărilor tensionale sau a conflictelor;</li>
                        <li> modul în care este perceput şi tratat copilul;</li>
                        <li> modalităţile de manifestare a autorităţii părinteşti (unitar sau diferenţiat);</li>
                        <li> gradul de acceptare şi tolerare a comportamentelor variate ale copilului;</li>
                        <li> modul de aplicare a sancţiunilor şi recompenselor;</li>
                        <li> gradul de deschidere şi sinceritate a copilului în raport cu părinţii;</li>
                        <li> satisfacţia resimţită de fiecare membru pentru viaţa de familie.</li>
                    </ul>
