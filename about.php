        <div class="row" id="cir4">
            <div class="box">
                <div class="col-lg-12">
                    <hr>
                    <h2 class="intro-text text-center">Abandonul scolar si delincventa juvenila</h2>
                    <hr>
                </div>
                <div class="col-md-6">
                    <img class="img-responsive img-border-left" src="img/abdDVJ.jpg" alt="">
                </div>
                <div class="col-md-6">
                    <p>Cumularea insatisfacţiilor generate de insuccesul şcolar generează atitudini evazioniste, manifeste în fuga de la şcoală şi, în ultimă instanţă, în abandon şcolar. Abandonul şcolar este expresia inadaptării elevului la cerinţele vieţii şcolare, dar indică în acelaşi timp şi incapacitatea şcolii de a se adapta la cerinţele şi nevoile tânărului.
Studiile care încearcă surprinderea relaţiei dintre abandonul şcolar şi delincvenţă au ca fundament teoretic teoria tensiunii sociale, în conformitate cu care abandonul şcolar ar implica o scădere a tensiunii, ceea ce ar avea ca efect scăderea frecvenţei conduitelor delincvente şi, la polul opus, teoria controlului social, care consideră că, dimpotrivă, abandonul determină, prin scăderea controlului social, o intensificare a conduitelor delincvente.</p>
                </div>
                <div class="col-md-12">
                    <p>Unele cercetări longitudinale (D. Elliot şi H. Voss, apud Neamţu, C., 2003, p. 206), care au urmărit evoluţia unor eşantioane reprezentative pe durata a patru ani, validează teoria tensiunii sociale, constatând că:</p>
                    <ul style="font-size:18px;">
                        <li>indicele delincvenţei este considerabil mai ridicat la tinerii care au abandonat şcoala, comparativ cu cei care şi-au continuat studiile;</li>
                        <li>natura experienţelor şcolare este motivul cel mai frecvent al conduitelor de devianţă şcolară, al abandonului în mod special;</li>
                        <li>indicele de delincvenţă al celor care abandonează şcoala descreşte în perioada imediat următoare plecării din şcoală, tendinţa de scădere rămânând constantă, în timp ce delincvenţa tinerilor care nu au abandonat şcoala creşte  constant în perioada celor patru ani de studiu. Explicaţia cercetătorilor ar fi aceea că părăsirea şcolii ar reduce frustrarea şi alienarea şi, astfel, s-ar reduce motivaţia pentru delincvenţă.</li>
                    </ul>
